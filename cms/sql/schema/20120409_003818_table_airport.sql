CREATE TABLE `airport` (
  `id` INT(11) NOT NULL AUTO_INCREMENT ,
  `idtype` INT(11) NOT NULL ,
  `idcountry` INT(11) NOT NULL ,
  `icao` VARCHAR(4) NULL DEFAULT NULL ,
  `iata` VARCHAR(3) NULL DEFAULT NULL ,
  `faa` VARCHAR(4) NULL DEFAULT NULL ,
  `ca` VARCHAR(4) NULL DEFAULT NULL ,
  `name` VARCHAR(255) NULL DEFAULT NULL ,
  `address` TEXT NULL DEFAULT NULL ,
  `city` VARCHAR(255) NULL DEFAULT NULL ,
  `state` VARCHAR(255) NULL DEFAULT NULL ,
  `zip` VARCHAR(50) NULL DEFAULT NULL ,
  `latitude` VARCHAR(255) NULL DEFAULT NULL ,
  `longitude` VARCHAR(255) NULL DEFAULT NULL ,
  `usefor` VARCHAR(25) NULL DEFAULT NULL ,
  `timeZone` VARCHAR(6) NULL DEFAULT NULL ,
  `georegion` VARCHAR(6) NULL DEFAULT NULL ,
  `dmslat` VARCHAR(255) NULL DEFAULT NULL ,
  `dmslon` VARCHAR(255) NULL DEFAULT NULL ,
  PRIMARY KEY (`id`))
ENGINE = InnoDB;
